/** Draw uppercase-folded text; unknown chars render as space. Returns right edge x. */
function label(canvas, text, x, y, color, scale = 1) {
  let cursor = x;
  for (const ch of String(text).toUpperCase()) {
    const glyph = GLYPHS[ch] || GLYPHS[' '];
    for (let gy = 0; gy < 5; gy++) {
      for (let gx = 0; gx < 3; gx++) {
        if (glyph[gy][gx] !== '1') continue;
        for (let sy = 0; sy < scale; sy++) {
          for (let sx = 0; sx < scale; sx++) {
            setPixel(canvas, cursor + gx * scale + sx, y + gy * scale + sy, color);
          }
        }
      }
    }
    cursor += (3 + 1) * scale;
  }
  return cursor - scale;
}

